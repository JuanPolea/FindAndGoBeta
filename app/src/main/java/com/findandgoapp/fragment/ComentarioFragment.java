package com.findandgoapp.fragment;

import android.app.Activity;
import android.app.ProgressDialog;
import android.content.Context;
import android.os.Bundle;
import android.support.v4.app.Fragment;
import android.util.Log;
import android.view.LayoutInflater;
import android.view.View;
import android.view.ViewGroup;
import android.widget.Button;
import android.widget.EditText;
import android.widget.Toast;

import com.android.volley.AuthFailureError;
import com.android.volley.Request;
import com.android.volley.RequestQueue;
import com.android.volley.Response;
import com.android.volley.VolleyError;
import com.android.volley.toolbox.StringRequest;
import com.android.volley.toolbox.Volley;
import com.findandgoapp.activity.R;
import com.findandgoapp.pojo.ComentarioPOJO;

import org.json.JSONException;
import org.json.JSONObject;

import java.util.HashMap;
import java.util.Map;


/**
 * A placeholder fragment containing a simple view.
 */
public class ComentarioFragment extends Fragment {

    private final ComentarioPOJO comentarioPOJO = new ComentarioPOJO();
    private ProgressDialog progressDialog;

    public ComentarioFragment() {
    }

    @Override
    public View onCreateView(LayoutInflater inflater, ViewGroup container,
                             Bundle savedInstanceState) {
        View rootView = inflater.inflate(R.layout.fragment_comentario, container, false);


        comentarioPOJO.setEtComenta((EditText) rootView.findViewById(R.id.idEtAddComentario));
        comentarioPOJO.setbComenta((Button) rootView.findViewById(R.id.idBAddComentario));
        comentarioPOJO.setbCancela((Button) rootView.findViewById(R.id.idBCancelaAddComentario));


        return rootView;
    }

    @Override
    public void onAttach(Context context) {
        super.onAttach(context);
        activity = context.getApplicationContext();


    }


    public void updateComentario(final Activity activity, final ComentarioPOJO comentarioPOJO) {

        progressDialog = new ProgressDialog(getActivity());
        progressDialog.setMessage(getString(R.string.sCargando));
        progressDialog.show();
        StringRequest stringRequest = new StringRequest(Request.Method.POST, activity.getResources().getString(R.string.sUrl_updateComentario),
                new Response.Listener<String>() {
                    @Override
                    public void onResponse(String response) {

                        progressDialog.dismiss();
                        try {
                            Log.e(getClass().getName(), response);
                            JSONObject jsonObject = new JSONObject(response);
                            int success;
                            success = jsonObject.getInt("success");

                            if (success == 1) {

                                Toast.makeText(activity, activity.getString(R.string.sUpdateComentario), Toast.LENGTH_SHORT).show();
                            } else {
                                Toast.makeText(activity, activity.getString(R.string.sNoUpdateComentario), Toast.LENGTH_SHORT).show();
                            }

                        } catch (JSONException e) {
                            e.printStackTrace();
                        }
                    }

                },
                new Response.ErrorListener() {
                    @Override
                    public void onErrorResponse(VolleyError error) {
                        //You can handle error here if you want
                        Log.e(getClass().getName(), error.getMessage());
                    }
                }) {
            @Override
            protected Map<String, String> getParams() throws AuthFailureError {
                Map<String, String> params = new HashMap<>();
                //Adding parameters to request

                return comentarioPOJO.toArray();
            }
        };

        //Adding the string request to the queue
        RequestQueue requestQueue = Volley.newRequestQueue(activity);
        requestQueue.add(stringRequest);
    }


}
