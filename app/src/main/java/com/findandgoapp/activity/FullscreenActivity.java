package com.findandgoapp.activity;

import android.content.Context;
import android.content.SharedPreferences;
import android.os.Bundle;
import android.support.v7.app.AppCompatActivity;
import android.support.v7.widget.Toolbar;
import android.util.Log;
import android.view.Menu;
import android.view.View;

public class FullscreenActivity extends AppCompatActivity {


    @Override
    protected void onCreate(Bundle savedInstanceState) {
        super.onCreate(savedInstanceState);

        setContentView(R.layout.activity_fullscreen);

        Toolbar toolbar = (Toolbar) findViewById(R.id.toolbar);
        setSupportActionBar(toolbar);
        SharedPreferences sharedPreferences = getApplicationContext().getSharedPreferences("DATOS", Context.MODE_PRIVATE);
        boolean isFotoNueva = sharedPreferences.getBoolean(getResources().getString(R.string.sFotoNueva), false);
        Log.e(getClass().getName(), "Foto Nueva " + isFotoNueva);
        boolean flag = sharedPreferences.getBoolean(getResources().getString(R.string.usuario), false);


        try {
            toolbar.setTitleTextColor(getApplicationContext().getResources().getColor(R.color.verdeAgua));

            if (flag)
                toolbar.setTitle(R.string.fotoPerfil);
            else
                this.setTitle(R.string.Leyenda);
            toolbar.setBackgroundColor(getApplicationContext().getResources().getColor(R.color.transparente));
            getSupportActionBar().setDisplayHomeAsUpEnabled(true);
            getSupportActionBar().setDisplayShowHomeEnabled(true);

            try {
                if (toolbar != null) {
                    toolbar.setTitle(getApplicationContext().getString(R.string.title_activity_menu_ppal));
                    toolbar.setNavigationOnClickListener(new View.OnClickListener() {
                        @Override
                        public void onClick(View view) {
                            //do something you want

                            onBackPressed();

                        }
                    });
                }
            } catch (ExceptionInInitializerError e) {
                e.printStackTrace();
            }


        } catch (NullPointerException e) {
            Log.e(getClass().getName(), e.getMessage());
        }
    }


    @Override
    protected void onPostCreate(Bundle savedInstanceState) {
        super.onPostCreate(savedInstanceState);

    }

    @Override
    public boolean onCreateOptionsMenu(Menu menu) {
        menu.clear();
        return super.onCreateOptionsMenu(menu);
    }

    @Override
    public void onBackPressed() {
        this.setTitle(R.string.title_activity_menu_ppal);


        super.onBackPressed();
    }


}
