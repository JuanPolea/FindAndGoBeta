package com.findandgoapp.activity;

import android.content.Context;
import android.content.Intent;
import android.content.SharedPreferences;
import android.os.Bundle;
import android.support.v7.app.AppCompatActivity;
import android.support.v7.widget.Toolbar;
import android.util.Log;
import android.view.View;


public class PenalizacionListado extends AppCompatActivity {


    private int idUsuario;

    @SuppressWarnings("ConstantConditions")
    @Override
    protected void onCreate(Bundle savedInstanceState) {
        super.onCreate(savedInstanceState);
        setContentView(R.layout.activity_penalizacion_listado);
        SharedPreferences sharedPreferences = getSharedPreferences(getString(R.string.DATOS), Context.MODE_PRIVATE);
        idUsuario = sharedPreferences.getInt(getString(R.string.sIdUsuario), 0);
        Toolbar toolbar = (Toolbar) findViewById(R.id.toolbar);

        setSupportActionBar(toolbar);
        toolbar.setTitleTextColor(getApplicationContext().getResources().getColor(R.color.verdeAgua));
        try {
            toolbar.setTitle(R.string.title_activity_penalizacion_listado);
            getSupportActionBar().setDisplayHomeAsUpEnabled(true);
            getSupportActionBar().setDisplayShowHomeEnabled(true);
            toolbar.setNavigationOnClickListener(new View.OnClickListener() {

                @Override
                public void onClick(View view) {


                    onBackPressed();
                }
            });
        } catch (NullPointerException e) {
            Log.e(getClass().getName(), e.getMessage());
        }


    }

    private void salir() {
        SharedPreferences sharedPreferences = getSharedPreferences(getString(R.string.DATOS), Context.MODE_PRIVATE);

        Intent intent = new Intent(this, AdminUsuarioPenalizar.class);
        SharedPreferences.Editor editor = sharedPreferences.edit();
        intent.addFlags(Intent.FLAG_ACTIVITY_NEW_TASK | Intent.FLAG_ACTIVITY_CLEAR_TOP | Intent.FLAG_ACTIVITY_NO_ANIMATION);

        editor.putInt(getString(R.string.sIdUsuario), idUsuario);


        editor.apply();
        startActivity(intent);
    }


    @Override
    public void onBackPressed() {
        super.onBackPressed();
        salir();
    }
}
